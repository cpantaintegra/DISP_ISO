package com.integrasystemsonline.Utilidades;

public class SubCol {

    private String name;

    private String data;

    public SubCol() {
    }

    public SubCol(String name, String data) {
        this.name = name;
        this.data = data;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getData() {
        return this.data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
